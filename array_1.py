listeler=["sevcan", "seda", "2555", "sevcansarikaya"]  # liste tanımlama

print(listeler)  # listeyi ekrana yazdırma
print(listeler[0])  # listenin ilk elemanını ekrana yazdırma
print(listeler[1:3])  # listenin 1. ve 2. elemanlarını ekrana yazdırma
print(listeler[-1])  # listenin son elemanını ekrana yazdırma
print(listeler[1:])  # listenin 1. elemanından sonrasını ekrana yazdırma
print(listeler[:3])  # listenin ilk 3 elemanını ekrana yazdırma
print(listeler[1:4])  # listenin 1. ile 3. elemanlarını ekrana yazdırma
print(listeler[1:4:2])  # listenin 1. ile 3. elemanları arasından 2'şer atlayarak ekrana yazdırma
print(listeler[-2:])  # listenin sondan 2. elemanından sonrasını ekrana yazdırma
print(listeler[-3:-1])  # listenin sondan 3. ile 2. elemanlarını ekrana yazdırma
print(listeler[-3:-1:2])  # listenin sondan 3. ile 2. elemanları arasından 2'şer atlayarak ekrana yazdırma
print(listeler[::-1])  # listenin elemanlarını ters çevirerek ekrana yazdırma
print(listeler[1:4:2][::-1])  # listenin 1. ile 3. elemanları arasından 2'şer atlayarak ve ters çevirerek ekrana yazdırma 

listeler.append("elma")  # listeye yeni bir eleman ekleme

